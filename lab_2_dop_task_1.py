a = int(input('Введите первый коэффицент '))
b = int(input('Введите второй коэффицент '))
c = int(input('Введите третий коэффицент '))


if a == 0:
  print('Не квадратное уравнение')
else:
  D = (b**2) - (4*a*c)
  if D < 0:
    print('Нет корней')
  elif D == 0:
    x = -b*0.5 / a
    print(f' Один корень {x}')
  else:
    f = D**0.5
    x1 = ((-b - f)*0.5) / a
    x2 = ((-b + f)*0.5) / a
    print(f'Первый корень {x1}, Второй корень {x2}')

  